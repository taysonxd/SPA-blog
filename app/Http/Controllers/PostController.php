    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show(Post $post)
    {
      $post->load(['category', 'tag']);

      if(!is_null($post->published_at) && request()->wantsJson())
        return $post;

      return view('views.spa');
    }

    public function postsByCategory(Category $category) {

      $posts = $category->posts()->with(['category', 'tag'])->whereNotNull('published_at')->paginate(3);

      if(request()->wantsjson())
        return $posts;

      return view('views.spa');
    }

    public function postsByTag(Tag $tag) {

      $posts = $tag->posts()->with(['category', 'tag'])->whereNotNull('published_at')->paginate(3);

      if(request()->wantsjson())
        return $posts;

      return view('views.spa');
    }

